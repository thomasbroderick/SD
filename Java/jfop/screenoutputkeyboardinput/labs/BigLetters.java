public class BigLetters {
  public static void main(String[] args) {
    System.out.println("TTTTT\tRRR  \tBBB  ");
    System.out.println("  T  \tR  R \tB  B ");
    System.out.println("  T  \tR R  \tB B  ");
    System.out.println("  T  \tRR   \tBB   ");
    System.out.println("  T  \tR R  \tB B  ");
    System.out.println("  T  \tR  R \tB  B ");
    System.out.println("  T  \tR   R\tB B  ");
  }
}
